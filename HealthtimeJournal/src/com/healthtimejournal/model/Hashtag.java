package com.healthtimejournal.model;

public class Hashtag {
	
	private int hashtag_id;
	private String hashtag_name;
	private int hashtag_category;
	
	
	public int getHashtag_id() {
		return hashtag_id;
	}
	
	public void setHashtag_id(int hashtag_id) {
		this.hashtag_id = hashtag_id;
	}
	
	public String getHashtag_name() {
		return hashtag_name;
	}
	
	public void setHashtag_name(String hashtag_name) {
		this.hashtag_name = hashtag_name;
	}
	
	public int getHashtag_category() {
		return hashtag_category;
	}
	
	public void setHashtag_category(int hashtag_category) {
		this.hashtag_category = hashtag_category;
	}
	
	

}
